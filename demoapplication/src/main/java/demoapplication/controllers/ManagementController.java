package demoapplication.controllers;

import demoapplication.aws.AwsServerOperations;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.ModelMap;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;

@Controller
@RequestMapping("/management")
public class ManagementController {
    @Autowired
    AwsServerOperations awsServerOperations;

    @GetMapping("/spawn/{number}")
    public String spawnMachines(ModelMap model, @PathVariable Integer number) {
        awsServerOperations.spawnMachines(number);
        model.put("spawn", number);
        return "spawnMachines";
    }
}
